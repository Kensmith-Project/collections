package holiday;

public class Holiday {
    private String name;
    private int day;
    private String month;

    public void Holiday(){

    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getDay() {
        return day;
    }

    public void setMonth(String month) {
        this.month = month;
    }
     public String getMonth(){
        return month;
     }
    public void setName(String name) {
        this.name = name;
    }
    public String getName(){
        return name;
    }
}
